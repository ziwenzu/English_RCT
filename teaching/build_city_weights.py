path = Path(__file__).parent / "city_weights.csv"
rows = []
with path.open(encoding="utf-8") as f:
    for row in csv.DictReader(f):
        if row["city_name"] in REMOVE:
            continue
        rows.append((row["city_name"], float(row["pop_wan"]), row["type"]))

existing = {x[0] for x in rows}
for name, pop in EXTRA:
    if name not in existing:
        rows.append((name, float(pop), "地级市"))
        existing.add(name)

raw = [x[1] for x in rows]
s = sum(raw)
exact = [TARGET * w / s for w in raw]
floors = [int(x) for x in exact]
rem = TARGET - sum(floors)
frac = sorted([(exact[i] - floors[i], i) for i in range(len(rows))], reverse=True)
for k in range(rem):
    floors[frac[k][1]] += 1

out = [(rows[i][0], floors[i], rows[i][2]) for i in range(len(rows))]
assert sum(x[1] for x in out) == TARGET

with path.open("w", encoding="utf-8", newline="") as f:
    w = csv.writer(f)
    w.writerow(["city_name", "pop_wan", "type"])
    for name, pw, typ in sorted(out, key=lambda x: -x[1]):
        w.writerow([name, int(pw), typ])

print("cities:", len(out), "sum_pop_wan:", sum(x[1] for x in out))
